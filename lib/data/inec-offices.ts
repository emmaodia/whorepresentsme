export function getStateOffice(state: string): INECStateOffice | undefined {
  return INEC_STATE_OFFICES.find(o => o.state === state)
}

export function getLGAOfficesForState(state: string): INECLGAOffice[] {
  return INEC_LGA_OFFICES.filter(o => o.state === state)
}

export function getLGAOffice(state: string, lga: string): INECLGAOffice | undefined {
  return INEC_LGA_OFFICES.find(o => o.state === state && o.lga === lga)
}
