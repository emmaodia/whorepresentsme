// ─── Helpers ────────────────────────────────────────────────────────────────

/** Get all unique states that have LGA data */
export function getStatesWithLGAs(): string[] {
  return [...new Set(LGA_MAP.map(l => l.state))].sort()
}

/** Get all LGAs for a given state */
export function getLGAsForState(state: string): LGAMapping[] {
  return LGA_MAP.filter(l => l.state === state).sort((a, b) => a.lga.localeCompare(b.lga))
}

/** Look up constituency mapping for a specific LGA */
export function getConstituencyForLGA(state: string, lga: string): LGAMapping | undefined {
  return LGA_MAP.find(l => l.state === state && l.lga === lga)
}
